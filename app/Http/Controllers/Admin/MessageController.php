     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Message::create([
            'broker_id'=>$request->broker_id,
            'user_id'=>$request->user_id,
            'message'=>$request->message,
            'write_broker'=>1,
            'user_status'=>1,
            'broker_status'=>0,
        ]);
        return response()->json([
            'alert' => 'success',
            'message' => 'Sent!',
        ]);
    }

